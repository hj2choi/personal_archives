#include <stdio.h>
#include <stdlib.h>
#include <sys/types.h>
#include <unistd.h>

int main(void) {
	printf("I am: %d\n", (int) getpid());

	pid_t pid = fork();
	printf("fork returned: %d\n", (int) pid);
	printf("I am: %d\n", (int) getpid());
	return EXIT_SUCCESS;
}
